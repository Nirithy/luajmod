    public static void printFunction(Prototype prototype, String level, PrintState state) {
        Prototype[] p = prototype.p;
        Print.printHeader(prototype, level);
        Print.printUpValues(prototype, level);
        Print.printCode(prototype, level, state);
        int offset = state.func;
        state.func = offset + p.length;
        for(int i = 0; i < p.length; ++i) {
            Print.ps.print("\n");
            Print.ps.print(level);
            Print.ps.print(".func F" + (offset + i) + ' ');
            Print.printFunction(p[i], level + "\t", state);
            Print.ps.print(level);
            Print.ps.print(".end ; F" + (offset + i) + "\n");
        }
    }

