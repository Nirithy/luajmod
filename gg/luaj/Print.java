    public static byte[] fix(Prototype f, int[] fixed) {
        int[] code = f.code;
        int n = code.length;
        if(fixed == null) {
            fixed = new int[n];
        }
        byte[] used = new byte[n + 2];
        for(int i = 0; i < 100 && Print.fix(used, f, code, fixed); ++i) {
            if(i == 0) {
                code = new int[n];
            }
            System.arraycopy(fixed, 0, code, 0, n);
            for(int j = 0; j < n; ++j) {
                used[j] = (byte)(used[j] & 0xFFFFFFE0);
            }
        }
        return used;
    }

    private static void format(String s, int maxcols) {
        int v1 = s.length();
        if(v1 > maxcols) {
            Print.ps.print(s.substring(0, maxcols));
            return;
        }
        Print.ps.print(s);
        int i = maxcols - v1;
        while(true) {
            --i;
            if(i < 0) {
                break;
            }
            Print.ps.print(' ');
        }
    }

    private static int getLine(Prototype f, int pc) {
        if(pc < 0) {
            return 0;
        }
        int[] lineinfo = f.lineinfo;
        if(lineinfo != null && pc < lineinfo.length) {
            int ret = lineinfo[pc];
            return ret >= 0 ? ret : 0;
        }
        return 0;
    }

